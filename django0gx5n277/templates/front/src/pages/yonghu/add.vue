	export default {
		data() {
			return {
				id: '',
				baseUrl: '',
				ro:{
					yonghuming : false,
					mima : false,
					xingming : false,
					touxiang : false,
					xingbie : false,
					nianling : false,
					youxiang : false,
					shouji : false,
				},
				type: '',
				userTableName: localStorage.getItem('UserTableName'),
				ruleForm: {
					yonghuming: '',
					mima: '',
					xingming: '',
					touxiang: '',
					xingbie: '',
					nianling: '',
					youxiang: '',
					shouji: '',
				},
				xingbieOptions: [],


				rules: {
					yonghuming: [
						{ required: true, message: '用户名不能为空', trigger: 'blur' },
					],
					mima: [
						{ required: true, message: '密码不能为空', trigger: 'blur' },
					],
					xingming: [
						{ required: true, message: '姓名不能为空', trigger: 'blur' },
					],
					touxiang: [
					],
